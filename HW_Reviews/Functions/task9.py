# Да се напише програма, която да брои четните и нечетните числа в даден списък от цели числа, използвайки lambda.

l = [1,2,3,4,5]

odds =  list(filter(lambda n:n%2,l))
evens = list(filter(lambda n:n%2==0,l))

print(f'Odds: {len(odds)}')
print(f'Evens: {len(evens)}')